</p><h5>Waarom wordt chinees nieuwjaar gevierd?</h5> <p>
Het Chinees Nieuwjaar wordt traditioneel gevierd met de drakendansen en leeuwendansen.

Volgens legenden was Nian ("Nyehn") een mensetend prooidier in het oude China, die ongemerkt huizen kon binnendringen. <br>
Al gauw leerden de Chinezen dat Nian gevoelig was voor hard lawaai en de kleur rood, en zij verdreven hem met explosies, <br>
vuurwerk, en veelvuldig gebruik van de kleur rood in het huis. Deze gebruiken leidden tot de eerste nieuwjaarsvieringen. 
