Het Chinese jaar Huangdi 4718, het Jaar van de Os, begint op <i>vrijdag 12 februari 2021</i>. <br>
Chinees Nieuwjaar, ook bekend als het Lentefestival of Lentefeest, is de belangrijkste van de Chinese feestdagen. <br>
Het wordt gevierd op de eerste tot en met de vijftiende dag van de eerste maand van de Chinese kalender. <br>
Het Chinese jaar begint op de tweede nieuwe maan na de zonnewende van 21 december. <br>
Dit kan niet voor 21 januari en niet na 20 februari gebeuren. Ook: het begin van het Lentefestival (Ch'un-chieh) of Xin Nian (Nieuwjaar). <br>
